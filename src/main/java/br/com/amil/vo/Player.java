package br.com.amil.vo;

public class Player {

	private String name;
	private String numDeath;
	private String numKill;
	private String award;
	private String sequencia;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getNumDeath() {
		return numDeath;
	}

	public void setNumDeath(String numDeath) {
		this.numDeath = numDeath;
	}

	public String getNumKill() {
		return numKill;
	}

	public void setNumKill(String numKill) {
		this.numKill = numKill;
	}

	public String getAward() {
		return award;
	}

	public void setAward(String award) {
		this.award = award;
	}

	public String getSequencia() {
		return sequencia;
	}

	public void setSequencia(String sequencia) {
		this.sequencia = sequencia;
	}

}
